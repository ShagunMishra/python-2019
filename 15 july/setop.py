s1={1,3,5,8,5}
s2={5,4,3,2,9}
print(s1,s2)
print(s1.union(s2))#operation on set works only on number
print(s1|s2) #to find union of two set
print(s1.intersection(s2))
print(s1&s2) #to find intersection of two set
print(s1.difference(s2))
print(s1-s2) #to find difference of two set
print(s2-s1)
print(s1.symmetric_difference(s2))#to find all value except duplicate
print(s1^s2)
